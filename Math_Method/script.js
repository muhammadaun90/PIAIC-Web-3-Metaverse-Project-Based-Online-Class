// Math.Ceil()
// Round up any decimal number to the nearest integer
var rounded = Math.ceil(0.5);
console.log(rounded); // result = 1
var rounded = Math.ceil(1.1);
console.log(rounded); // result = 2
var rounded = Math.ceil(2.5);
console.log(rounded); // result = 3