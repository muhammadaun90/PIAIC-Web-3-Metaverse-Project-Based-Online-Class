// Sorting in arrays
let names = ["Aun", "Shahzad", "Zain", "Rabab", "Tabish"];
names.sort(); // It sorts names alphabetically
console.log(names);

// Reversing in arrays
let foods = ["Biryani", "Pizza", "Tikka", "Sandwich"];
foods.reverse(); // It reverses from last element to first
console.log(foods);